#insiemi

set1 = {1,2,3,4,5}
set2 = {4,5,6,7,8}

print(set1.union(set2))  # output : {1, 2, 3, 4, 5, 6, 7, 8}
print(set1.intersection(set2)) # output : {4, 5}
print(set1.difference(set2)) # output : {1, 2, 3}  
print(set1.symmetric_difference(set2)) # output : {1, 2, 3, 6, 7, 8} 




